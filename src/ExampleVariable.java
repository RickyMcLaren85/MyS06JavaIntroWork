public class ExampleVariable {
    public static void main(String[] args) {
        // introducing Harry Potter person
        String firstName = "Solid"; // string data type = contains text
        String lastName = "Snake";
        int age = 35; // int data type = contains whole number
        char gender = 'U'; // char data type = contains a single character
        Boolean isonmission = true; // boolean data type = contains true and false
        float weight = 85.6f; // double data type = contains decimal numbers

        System.out.println("Agent first name = " + firstName);
        System.out.println("Agent last name= " + lastName);
        System.out.println("Agent age = " + age);
        System.out.println("Agent gender = " + gender);
        System.out.println("Is Agent on a mission? " + isonmission);
    }
}
